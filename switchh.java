class switchh
{
	public static void main(String args[])
	{
		int i=2;
		switch(i)
		{
			default : 
			System.out.println("hello default");
			case 1: 
			System.out.println("hello1");
			case 2: 
			System.out.println("hello2");
			//break;
			case 3: 
			System.out.println("hello3");
			
		}
		
	}
}