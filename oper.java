class oper
{
	public static void main(String args[])
	{
		int a=10%3;
		int b=10%-3;
		int c=-10%3;
		int d=-10%-3;
		System.out.println(" a= "+a);
		System.out.println(" b= "+b);
		System.out.println(" c= "+c);
		System.out.println(" d= "+d);
		
		/* O/P is always corresponding to the 
		    sign of NUMERATOR
		*/
	}
}